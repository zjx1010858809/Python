import turtle

def main():

    turtle.pensize(4)
    turtle.pencolor('red')


    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.left(90)
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(100)
    turtle.right(135)
    turtle.forward(50)

    turtle.mainloop()

if __name__ == '__main__':
    main()